import 'package:flutter/material.dart';

// Cung cấp ngữ cảnh ngôn ngữ cho toàn ứng dụng
class LanguageProvider extends InheritedWidget {
  final Locale currentLocale;
  final String currentLanguageCode;
  final Function(String) changeLanguage;
  final String Function(String) getLanguageName;
  final String Function(String) translate;
  
  const LanguageProvider({
    super.key,
    required this.currentLocale,
    required this.currentLanguageCode,
    required this.changeLanguage,
    required this.getLanguageName,
    required this.translate,
    required super.child,
  });
  
  // Truy cập tiện lợi tới LanguageProvider trong cây widget
  static LanguageProvider? of(BuildContext context) {
    return context.dependOnInheritedWidgetOfExactType<LanguageProvider>();
  }
  
  @override
  // Khi mã ngôn ngữ thay đổi thì các widget con cần rebuild
  bool updateShouldNotify(LanguageProvider oldWidget) {
    return currentLanguageCode != oldWidget.currentLanguageCode;
  }
}

