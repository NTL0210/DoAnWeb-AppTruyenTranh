  static Locale _currentLocale = const Locale('en', 'US');
  static VoidCallback? _onLanguageChanged;
  
  static Locale get currentLocale => _currentLocale;
  
  static String get currentLanguageCode => _currentLocale.languageCode;
  
  static void setOnLanguageChanged(VoidCallback callback) {
    _onLanguageChanged = callback;
  }
  
  // Đổi ngôn ngữ theo mã (vi, en, es, fr)
  static void changeLanguage(String languageCode) {
    switch (languageCode) {
      case 'vi':
        _currentLocale = const Locale('vi', 'VN');
        break;
      case 'es':
        _currentLocale = const Locale('es', 'ES');
        break;
      case 'fr':
        _currentLocale = const Locale('fr', 'FR');
        break;
      default:
        _currentLocale = const Locale('en', 'US');
    }
    // Gọi callback để rebuild ứng dụng
    _onLanguageChanged?.call();
  }
  
  // Trả về tên hiển thị của ngôn ngữ
  static String getLanguageName(String languageCode) {
    switch (languageCode) {
      case 'en':
        return 'English';
      case 'vi':
        return 'Tiếng Việt';
      case 'es':
        return 'Español';
      case 'fr':
        return 'Français';
      default:
        return 'English';
    }
  }
  
  // Hàm dịch khoá chuỗi dựa vào ngôn ngữ hiện tại
