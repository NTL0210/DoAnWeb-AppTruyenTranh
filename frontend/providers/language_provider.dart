    switch (_currentLocale.languageCode) {
      case 'vi':
        return _vietnameseTranslations[key] ?? key;
      case 'es':
        return _spanishTranslations[key] ?? key;
      case 'fr':
        return _frenchTranslations[key] ?? key;
      default:
        return _englishTranslations[key] ?? key;
    }
  }
  
